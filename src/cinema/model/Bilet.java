package cinema.model;

public class Bilet {

	private Saat saat;
	private Salon salon;
	private int x;
	private int y;
	
	public Saat getSaat() {
		return saat;
	}
	public void setSaat(Saat saat) {
		this.saat = saat;
	}
	public Salon getSalon() {
		return salon;
	}
	public void setSalon(Salon salon) {
		this.salon = salon;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
}
